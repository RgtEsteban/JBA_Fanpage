                        <h4 class="soustitre">Apparence du personnage</h4><br>
                        <p>Polnareff est un homme de taille supérieure à la moyenne, doté d'une musculature bien 
                            développée. Ses cheveux argentés montent tout droit au-dessus de sa tête à une hauteur 
                            inhabituelle. Il porte aussi des boucles d'oreilles en forme de moitiés de cœur brisé.
                        </p>
                        <h4 class="soustitre">Personnalité</h4><br>
                        <p>
                            Polnareff est le maladroit au bon cœur typique. Il est héroïque et protecteur envers 
                            ceux qu'il aime, il a montré des stratégies assez impressionnantes tout au long de la 
                            série. Il est impulsif et bruyant, il plonge rapidement dans l'action souvent sans avoir 
                            évalué les risques, ce qui lui a valu de nombreux problèmes. Malgré tout, il se dédit 
                            sincèrement à faire le bien et ses amis peuvent compter sur lui. Il se prétend être un 
                            séducteur, mais son caractère implique qu'il surestime ses véritables prouesses. Sur 
                            l'ensemble des personnages de la série, Polnareff est le moins réticent à montrer ses 
                            émotions, il ajoute beaucoup d'humour et de légèreté là où on aurait eu une équipe assez 
                            sérieuse (surtout avec Kakyoin et Jotaro qui sont des personnages très froids). En effet, 
                            même s'il peut parfois être vu comme le rigolo du groupe, les compagnons de Polnareff 
                            apprécient sa nature et lui font confiance pour les aider à vaincre lorsqu'ils sont 
                            confrontés à une bataille difficile. De manière intéressante, les meilleurs amis de 
                            Polnareff pendant cette partie sont Abdul et Iggy, deux personnages à qui il ne put 
                            tenir tête lors de leur première rencontre. En fait, aucun personnage ne pleure la mort 
                            de ses compagnons comme le fait Polnareff, et aucun personnage n'est aussi dédié à les 
                            venger.
                        </p>
                        <h4 class="soustitre">Son stand</h4><br>
                        Silver Chariot se présente comme un guerrier en armure d'argent, comme un véritable 
                        chevalier. La chevalerie de Polnareff se montre dans la façon dont il traite ses amis 
                        : avec honneur et loyauté. Son Stand représente la carte de tarot « Le Chariot », qui 
                        symbolise la conquête et la victoire, et il est presque entièrement basé sur des 
                        techniques de combat rapproché. Lorsque Polnareff apparaît pour la première fois, 
                        il envahit l'espace privé du groupe Joestar, les défiant en plein milieu de leur 
                        conversation. Même si Polnareff est habituellement imprudent, il s'est montré capable 
                        de remporter la victoire par lui-même, prouvant sa valeur en battant ses ennemis seuls 
                        à plusieurs occasions. Le Chariot peut aussi être interprété comme la résolution d'un 
                        conflit avec soi-même et la découverte de l'équilibre nécessaire pour accomplir ses 
                        ambitions, représenté par les émotions instables de Polnareff alors qu'il tente de 
                        venger sa sœur.
